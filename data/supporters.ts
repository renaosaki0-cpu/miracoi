export const supporters: Supporter[] = supportersRaw
  .map((supporter, index) => ({ supporter, index }))
  .sort((a, b) => {
    const diff = getTitleRank(b.supporter.title) - getTitleRank(a.supporter.title);
    return diff !== 0 ? diff : a.index - b.index;
  })
  .map(({ supporter }) => ({
    ...supporter,
    image: resolveSupporterImageById(supporter.id),
  }));
